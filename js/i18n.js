let currentLang = localStorage.getItem('latwo_lang') || 'uk';

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || key;
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('latwo_lang', lang);
  applyTranslations();
  updateLangUI();
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    const isHtml = el.hasAttribute('data-i18n-html');
    const val = t(key);
    if (attr) {
      el.setAttribute(attr, val);
    } else if (isHtml) {
      el.innerHTML = String(val).replace(/\n/g, '<br>');
    } else {
      el.textContent = val;
    }
  });

  // Update html lang attribute
  document.documentElement.lang = currentLang === 'uk' ? 'uk' : 'en';
}

function updateLangUI() {
  const currentLabel = document.getElementById('current-lang');
  if (currentLabel) {
    currentLabel.textContent = currentLang === 'uk' ? 'Ukrainian' : 'English';
  }

  // Update active state in dropdown
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.getAttribute('data-lang') === currentLang);
  });
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  updateLangUI();

  // Bind lang options
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.addEventListener('click', (e) => {
      e.preventDefault();
      const lang = opt.getAttribute('data-lang');
      setLanguage(lang);
      const dropdown = document.getElementById('lang-dropdown');
      if (dropdown) dropdown.classList.remove('open');
    });
  });
});
